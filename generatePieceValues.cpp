string generateValueTable(const ValueTable& valueTable)
{
    stringstream result;

    result << "pieceValue(AllPieces,[_,_,"
           << valueTable.piece
           << ",white],"
           << -valueTable.pieceValue
           << ")."
           << endl;

    result << "pieceValue(AllPieces,[_,_,"
           << valueTable.piece
           << ",black],"
           << valueTable.pieceValue
           << ")."
           << endl;

    for (int i = 0; i < 64; i++) {
        result << "pieceValue(AllPieces,["
               << ((i % 8) + 1)
               << ","
               << ((i / 8) + 1)
               << ","
               << valueTable.piece
               << ",white],"
               << -valueTable.values[i%8 + ((63-i)/8)*8]
               << ")."
               << endl;

        result << "pieceValue(AllPieces,["
               << ((i % 8) + 1)
               << ","
               << ((i / 8) + 1)
               << ","
               << valueTable.piece
               << ",black],"
               << valueTable.values[i]
               << ")."
               << endl;
    }

    return result.str();
}

